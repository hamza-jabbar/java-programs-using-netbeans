public class DoWhileLoop {
    public static void main(String[] args) {
        int counter = 110;
        
        do{
            System.out.println(counter);
            counter++;
        }while(counter <=10);
        System.out.println(" ");
        System.out.println("#DGF");
    }
}
/*Only does this loop while the counter is less than or equal to 10*/
